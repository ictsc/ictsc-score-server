<template>
  <div class="content">
    <h2>{{ category.title }}</h2>

    <div class="columns is-multiline is-mobile is-2 is-variable">
      <problem-card
        v-for="problem in problems"
        :key="problem.id"
        :problem="problem"
      />
    </div>
  </div>
</template>

<script>
import ProblemCard from '~/components/molecules/ProblemCard'

export default {
  name: 'CategoryColumn',
  components: {
    ProblemCard
  },
  props: {
    category: {
      type: Object,
      required: true
    }
  },
  computed: {
    problems() {
      return this.sortByOrder(this.category.problems)
    }
  }
}
</script>

